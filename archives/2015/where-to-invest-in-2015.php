                    <p>
                      Good bye 2014! A historical year when the Bharatiya Janata Party, under the forceful and dynamic leadership of Narendra Modi, wrote a new chapter in the political history of the country by capturing Delhi with a comfortable and clear majority, throwing out of reckoning the grand old party in the country. Assembly elections held in several states since then showed that the Modi magic still works. The party succeeded in capturing vital states like Maharashtra, Rajasthan, Haryana and Jharkhand, and put up its best ever performance in Jammu & Kashmir. The dramatic change of far reaching significance rekindled hope all around at home and abroad and sent the stock market indices soaring to record peaks
                    </p>
                    <p>
                      The mood on the eve of the New Year 2015, however, is optimistic but a little wary. ‘Will the hopes and aspirations be realized?’ people ask. More than 200 days have passed but the impact of the Modi wave is still not visible on the economic front. Of course, Mr. Modi is busy globetrotting, visiting one country after another while his mentors and supporters are busy announcing India as a Hindu Rashtra and organising “Ghar Wapasi’ programmes to increase Hindu population. At home, Mr. Modi is initiating a clean India movement (Swachcha Bharat Abhiyan). There have been moves to ‘reform’ the education system and award ‘Bharat Ratna’ honour to people close to the new dispensation
                    </p>
                    <p>
                       With secondary objectives getting top priority, the primary objective of uplifting the economic and social life of the masses has been sidelined. The country cannot afford such luxurious transgressions! The economic scenario is far from satisfactory. The economy is still reeling under the severe problems created by the policy paralysis of the previous UPA government which created new records in corruption and nepotism. Even after 200 days of the Modi government, industrial production slid down by 4.2 per cent in October 2014 over the same month in the previous year when the UPA was in power. This is the sharpest fall in the last three years. The debacle followed a marked 7.6 per cent drop in the manufacturing sector and 2.3 per cent fall in capital goods output. The trade deficit has crossed $100 billion mark with the country suffering from unfavourable trade balance with over 80 countries. What is more, prospects for an early revival of the economy are dimmed with the GDP growth moving down to 5.3 per cent in the second quarter (July to September 2014) of the current fiscal from 5.7 per cent growth clocked in the first quarter. 
                    </p>
					<p>
					 Though price indices are reported to be coming down, prices of essential commodities are still ruling at a very high level, in fact simply unaffordable for millions of people.
					</p>
					<p>
					 Industrialists who donated liberally to the BJP are very much perturbed at the virtual inaction of the government on speeding the pace of economic reforms and the growth of capital formation. Figures for the last 10 months show that investments have fallen in important sectors like metallurgical industries, electrical equipment, industrial machinery and cement. What is more, the national income numbers show that gross fixed capital formation has slipped to 28.5 per cent of GDP in the first half of the current fiscal from 29.3 per cent in the first half of the last year.
					</p>
					<p>
					 The circumstances are quite favourable for the economy with a reform-friendly party like BJP getting a clear majority, crude oil prices literally slumping, helping the Modi government to bring down the current account deficit as well as fiscal deficit and lending strength to the rupee against a rising dollar. These factors could have been used to put the economy on a more even keel. Sadly, the government is dillydallying on economic front, while keeping itself busy with secondary issues. 
					</p>
					<p>The New Year brings lot of hopes. We welcome the year 2015 with a fervent hope that Mr. Modi the great showman also emerges as a doer and takes the country to the top among the comity of nations.</p>
