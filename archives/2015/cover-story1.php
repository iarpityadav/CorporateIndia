				 <h4>WHERE TO INVEST IN 2015?</h4>
				 <p align=""> 2014 was a watershed. It was dominated by politics. In 2015 the focus will be on the economy, national and global. 2015 will be a year of challenges. The
						economy is not out of woods. Fiscal deficit is worryingly high. But political stability, an alert RBI and decline in global commodity prices would serve India well. The strong dollar and impending rate hike by US Fed in face of downtrend in other major
						economies could hit emerging markets on exports and capital flows. The next six months is not a time to take undue risks in your portfolio. We suggest a few scrips which are likely to outperform the market and provide decent returns.
				 </p>		
