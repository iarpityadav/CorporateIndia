			 <div class="coversection2" align="center">
				 <p align="left"><img src="<?php echo $baseUrl; ?>images/coverstory3.jpg" class="img-responsive" align="right"/> 
					 2014 will go down the history as a watershed. In the most bitterly contested Lok Sabha elections since 1977,Narendra Modi drove the BJP-led NDA to a resounding victoryon the promise of political stability and economic dynamism with a focus on reforms, faster growth and jobs. Financial
					 markets had started betting on his victory even before elections were formally announced. They went ecstatic after the results, pushing the Sensex to new all-time peaks and backing the rupee to stand up to the mighty dollar.
				 </p>
				 <p align="left">The BSE Sensex touched the peak of 28,822 on 28
					November before the closing the year at 27,403.54 with a gain of 6201.16 points or 29 per cent. The small and midcap stocks yielded even higher returns, with BSE mid-cap
					index closing 49 per cent higher and the small cap index gaining almost 67 per cent. Interestingly, the FII preference shifted from equity to debt. FII investment in equities plummeted
					to $16.494 billion in 2014, compared with $40.2 billion in 2013, where that in debt soared to $26.18 billion from a negative $15.94 billion in 2013.
				 </p>
				 <p align="left">
					 In contrast, gold and silver disappointed once again giving negative returns for the third year in a row. Gold closed the year at $1195 an ounce, down from $1240 a year ago and silver finished at $16.08 an ounce, compared with $19.1 a year ago.
				 </p>
				 <p align="left">
					 As the year comes to a close, the euphoria has started receding. Some of Mr. Modi’s most vocal supporters ranging from leading industrialists and NRIs visiting home to middle class housewives in cities have started complaining that he
					 is to yet to deliver on his tall promises. To be sure, the overall mood is still highly optimistic and Mr. Modi’s popularity has gone up since he assumed office.
				 </p>
				 <p align="left">
					 2015 promises to be a year of challenges. The first challenge is to accelerate the growth momentum which alone can
					 generate millions of growth that Modi promised. The economy is still struggling to come out of the morass into which it had fallen in the UPA reign. The international situation is likely to throw up some serious challenges in the areas of exports andm capital flows. The economic growth is expected to inch up to 5.5 per cent this year, compared with below 5 per cent last year. However, the expectations of a return to heady days of 8-9 per cent growth are fast receding.
				 </p>
				 <p align="left">The recent GDP numbers show that the economy is still not out of woods. For the quarter ended September, India’s  gross domestic product (GDP) grew 5.3 per cent, compared with 5.7 per cent in the previous quarter. Industrial production
					remains stagnated. The index of industrial productiondeclined by 4.2 per cent in October,as against minus 1.2 percent in October 2013. The ‘low base’ points to a dire situation,belying claims that the worst is over.
				 </p>
				 <p align="left">The government has tried its utmost to clear supply bottlenecks, delivering a sentiment-booster for India Inc. The prime minister has personally assumed chairmanship of the project
					monitoring group to expedite clearances. But a supply-side push will require support from the demand side, which is not in evidence. Consumer demand, investment and exports all remain weak. Growth data points to a near-stagnation in investment
					in a solute terms. Corporates are faced with low utilisaiton of capacities and mountains of debt; banks are reeling under NPAs and capital adequacy issues.	
				</p>
				 <p align="left">
					 Another challenge is the widening fiscal deficit, which reached nearly 90 per cent of the Budget Estimate for the fiscal in the first seven months itself. The Government has set a limit of Rs. 5.31 lakh crore for the full year but the
					deficit reached Rs. 4.76 lakh crore in the April-October period. The key concern on the fiscal deficit front is the dipping evenues.
					Tax receipts grew by just 3.46 per cent against the 19.8 per cent target. Total indirect tax collections for AprilOctober 2014 are up by a nominal 3.46 per cent (net of states’ share). If one adjusts for inflation, the tax collection growth would be close to zero.
				 </p>
				 <p align="left">
					 The government may have to slash budgeted expenditureby about Rs. 1 lakh crore if were to stick to the fiscal deficittarget of 4.1 per cent of the GDP for the current year.
					 The axe is likely to fall on capital expenditure, besides social schemes, which in turn could hurt growth.
				 </p>
				 <p align="left">
					 On the positive side, inflation has dropped consider- ably. The consumer price index numbers for November at 4.38 per cent, with food inflation at just 3.14 per cent. The WPI inflation in November dropped to zero. Indeed, if global
					 commodities, especially oil, continue to fall, WPI could even go into the negative territory: primary commodities contracted 0.98 per cent and fuel/power fell 4.91 per cent in November.
				 </p>
				 <p align="left">While the drop in inflation may act as a consolation, there is no surprise here. The fall has been exaggerated by the ‘high base’ effect, or double-digit rates of inflation last year. Besides, it may also be reflection of the absence of
				    growth and job creation.
				 </p>
				 <p align="left">
					 The international situation is developing in a manner that could pose major challenge to India in the coming year.
				 </p>
				 <p align="left">
					 The US recovery is gaining ground. The US economy grew at 4.2 per cent in the last two quarters and the jobless rate is down to 5.8 per cent. The US Fed is likely to raise interest rates by the middle of the next year. Probably, it
					 would have raised them earlier but for the weakness in other major economies. China is slowing down, Japan is in recession and Europe in struggling to shake off its stagnation through loose monetary policy. Oil prices have tumbled in the world market, from $115 per barrel in June to $57 now
					 owing to weak demand and oversupply. Russia is in a major trouble with zero growth, high inflation and the ruble plunging  against dollar.
				 </p>
				 <p align="left">
					 As a result, dollar has strengthened against all major currencies. The rupee, which had held ground for a few months, is now down to Rs. 63.11 and is expected to stabilize between 64 and 65 to a dollar.
					 The outlook for the Indian currency in the coming months remains weak
				 </p>
				 <p align="left">
					 As an oil importer, India is delighted with the fall in oil prices. However, if the fall forces some oil exporters to default on their external commitments, it might trigger a broader
					 crisis engulfing all emerging markets including India. Risk aversion among investors may lead to withdrawal of capital form emerging markets, hitting their currencies and financial markets.
				 </p>
				 <p align="left">
					 <h4>  2015: Global growth to accelerate</h4><img src="<?php echo $baseUrl; ?>images/coverstory4.jpg" class="img-responsive" align="center"/><h4>(Goldman sachs)</h4>
				 </p>
				 <p align="left">
					 West Asian oil exporters are major destinations for Indian exports and labour, so crashing oil prices can mean a steep fall in India’s exports to and remittances from the Gulf.
					 The same could be said for Europe and China. This will be offset by cheaper oil imports, but the equilibrium may be reached at a slower GDP growth rate.
				 </p>
				 <p align="left">
					 Finally, global finance may be poised to exit from emerging markets anyway with the end of quantitative easing and expected rise in US interest rates in mid-2015.
				 </p>
				 <p align="left">
					 2015, therefore, promises to be a challenging year. However, there is no reason for panic. We have a politically stable government headed by a prime minister who is not only charismatic but also an able administrator and a visionary leader. An alert Reserve Bank of India and the decline
					 in commodity prices are other positives for the economy.
				 </p>
				 <p align="left">
						Reforms have been initiated in most of the areas that require improvement. The recent ordinances on coal mine auctions and FDI in insurance have gone down well with the investors. An ordinance making major changes in the land
						acquisition law is next in line. The prime minister has assumed chairmanship of the Project Monitoring Group to expedite and revive stalled projects. Efforts are being made towards easing the conduct of business through greater use of technology,
						facilitating labour law improvements and developing worldclass infrastructure. E-biz platform and the Digital India campaign are going to play a vital role in integrating all government
						department and services. The development of industrial corridors, 100 smart cities, opening up of Railways infrastructure to FDI, plans for bullet trains, the Sagarmala project for development of ports, development of low-cost airports, etc, will play a critical
						role in enhancing business competitiveness, triggering economic activity and generating huge employment. The government’s initiatives on these fronts are likely to show results in the year 2015.
						The next trigger for stock markets would be the budget. If Mr. Jaitley meets the expectations of the business and industry, the economy will take off and the bullish sentiment will be reinforced. However, if his next budget is on the same line
						as the last one, it will be hard to avoid disappointment and even disillusionment.
				 </p>
